def get_all_prompts():
    """모든 프롬프트를 반환 (총 50개)"""
    return SHORT_PROMPTS + MEDIUM_PROMPTS + LONG_PROMPTS

def get_prompts_by_length(length_type):
    """길이별 프롬프트 반환"""
    if length_type == "short":
        return SHORT_PROMPTS
    elif length_type == "medium": 
        return MEDIUM_PROMPTS
    elif length_type == "long":
        return LONG_PROMPTS
    else:
        return get_all_prompts()

def get_formatted_prompt(user_prompt, include_system=True):
    """시스템 프롬프트와 함께 포맷된 프롬프트 반환"""
    if include_system:
        return f"{SYSTEM_PROMPT}\n\n사용자 질문: {user_prompt}"
    else:
        return user_prompt

